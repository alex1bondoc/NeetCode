# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right

class Solution:
    def kthSmallest(self, root: Optional[TreeNode], k: int) -> int:
        sol = -1001
        def dfs(node):
            if not node:
                return 
            if node.left:
                dfs(node.left)
            nonlocal k
            k -= 1
            if k == 0:
                nonlocal sol
                sol = node.val
            if node.right:
                dfs(node.right)
        dfs(root)
        if sol == -1001:
            return None
        return sol
